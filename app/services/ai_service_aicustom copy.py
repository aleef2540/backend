async def detect_intent(user_message: str, course_data) -> dict:
    system_prompt = f"""
คุณคือ AI ที่ทำหน้าที่จำแนก intent ของข้อความผู้ใช้ และ topic ที่ผู้ใช้พูดถึง

intent ให้เลือกเพียง 1 ค่า:
- greeting = ข้อความทักทาย เช่น สวัสดี hello hi
- general = คุยทั่วไป ยังไม่ชัดว่าอยากพัฒนาอะไร
- learning = ผู้ใช้กำลังพูดถึงการพัฒนาตัวเอง หรืออยากเก่งขึ้น

topic ให้เลือกเพียง 1 ค่า จากรายการนี้เท่านั้น:
{course_data}

ถ้าไม่ตรงเลยให้ตอบเป็น:
unknown

ตัวอย่าง:
greeting:
- สวัสดี
- hello

general:
- วันนี้เหนื่อยมาก
- เพิ่งประชุมมา

learning:
- อยากพัฒนาการสื่อสาร
- พูดในที่ประชุมไม่มั่นใจ
- อยากเก่งขึ้นเรื่องการนำเสนอ

กฎ:
- ถ้ามีคำว่า "อยากพัฒนา" / "อยากเก่งขึ้น" / "ปัญหาในการทำงาน" → learning
- ถ้าเป็นแค่การทักทาย → greeting
- ถ้าไม่ชัด → general
- topic ต้องเลือกจากรายการที่ให้เท่านั้น
- ถ้าไม่มี topic ที่ตรง ให้ตอบเป็น unknown

ตอบเป็น JSON เท่านั้น ห้ามมีข้อความอื่น:
{{
  "intent": "greeting|general|learning",
  "topic": "ชื่อ topic จากรายการ หรือ unknown"
}}
""".strip()

    result = await call_openai_chat_full(
        model="gpt-4.1-mini",
        system_prompt=system_prompt,
        user_prompt=user_message,
        temperature=0.1,
    )

    text = (result.get("content") or "").strip()
    text = clean_json(text)

    intent = "general"
    topic = "unknown"

    try:
        data = json.loads(text)
        intent = str(data.get("intent", "general")).strip().lower()
        topic = str(data.get("topic", "unknown")).strip()
    except Exception:
        pass

    if intent not in ["greeting", "general", "learning"]:
        intent = "general"

    if not topic:
        topic = "unknown"

    return {
        "intent": intent,
        "topic": topic,
    }

